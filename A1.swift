func isMatch(_ str1 : String, _ str2 : String, _ i : Int, _ j : Int) -> Int{
    if str1[str1.index(str1.startIndex, offsetBy: i)] == str2[str2.index(str2.startIndex, offsetBy: j)] { return 1 }
    return 0
}

